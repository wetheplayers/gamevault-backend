export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const [user, setUser] = useState<{ name: string; email: string; avatar: string }>({
    name: "User",
    email: "",
    avatar: "",
  })

  useEffect(() => {
    const loadUser = async () => {
      const supabase = createClient()
      const {
        data: { user },
      } = await supabase.auth.getUser()

      if (!user) return

      // Kept for future extension; values are derived below
      // eslint-disable-next-line @typescript-eslint/no-unused-vars
      const displayName: string | undefined = undefined
      // eslint-disable-next-line @typescript-eslint/no-unused-vars
      const avatarUrl: string | undefined = undefined

      const { data: profile } = await supabase
        .from("user_profiles")
        .select("display_name, avatar_url")
        .eq("id", user.id)
        .single()

      const resolvedDisplayName = profile?.display_name ?? user.user_metadata?.display_name
      const resolvedAvatarUrl = profile?.avatar_url ?? user.user_metadata?.avatar_url

      setUser({
        name: resolvedDisplayName || user.email?.split("@")[0] || "User",
        email: user.email || "",
        avatar: resolvedAvatarUrl || "",
      })
    }

    loadUser()
  }, [])

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <IconInnerShadowTop className="!size-5" />
                <span className="text-base font-semibold">GameVault</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavDocuments items={data.documents} />
        <NavSecondary items={data.navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={user} />
      </SidebarFooter>
    </Sidebar>
  )
}
